// 사용자로부터 두개의 정수를 입력받아서
// 사칙연산에 대한 결과를 출력하는 프로그램을 작성하라.

public class Quiz0301
{
	public static void main(String[] args)
	{
		int num1 = 24;
		int num2 = 3;
		
		System.out.println("num1 + num2 = " + (num1 + num2));
		System.out.println("num1 - num2 = " + (num1 - num2));
		System.out.println("num1 * num2 = " + (num1 * num2));
		System.out.println("num1 / num2 = " + (num1 / num2));		
	}
}
